    private void btn_decodefileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_decodefileActionPerformed
        
        File encriptado = new File(carpeta + namefinal);
        String extension = "";
        int i = nombrearchivo.lastIndexOf('.');
        if (i > 0) {
            extension = nombrearchivo.substring(i + 1);
        }

        File desencriptado = new File(carpeta + namefinaldecrypted +"." + extension);
        try {
            encriptararchivo.proc(contrasena, Cipher.DECRYPT_MODE, encriptado, desencriptado);
            JOptionPane.showMessageDialog(null, "Archivo Desencriptado");
        } catch (InvalidKeyException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeySpecException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchPaddingException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_decodefileActionPerformed

