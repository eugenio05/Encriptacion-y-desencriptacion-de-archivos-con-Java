    private void btn_cargarfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cargarfileActionPerformed

        seleccionarArchivo = new JFileChooser();
        seleccionarArchivo.showOpenDialog(null);
        nombrearchivo = seleccionarArchivo.getSelectedFile().getAbsolutePath();
        patharchivo.setText(nombrearchivo);
        filename = FilenameUtils.removeExtension(seleccionarArchivo.getSelectedFile().getName());
        namefinal = "//" + filename + ".encrypted";
        namefinaldecrypted = "//" + filename + "_desencriptado";
    }//GEN-LAST:event_btn_cargarfileActionPerformed

    private void btn_encodefileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_encodefileActionPerformed

        File plano = new File(seleccionarArchivo.getSelectedFile().getAbsolutePath());

       
        File encriptado = new File(carpeta + namefinal);
        try {
            encriptararchivo.proc(contrasena, Cipher.ENCRYPT_MODE, plano, encriptado);
            JOptionPane.showMessageDialog(null, "Archivo Encriptado");
        } catch (InvalidKeyException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeySpecException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchPaddingException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Archivos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btn_encodefileActionPerformed

